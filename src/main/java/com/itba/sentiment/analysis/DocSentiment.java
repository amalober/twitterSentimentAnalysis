package com.itba.sentiment.analysis;

public class DocSentiment {
	
	String type;
	Double score;
	int mixed;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Double getScore() {
		return score;
	}
	public void setScore(Double score) {
		this.score = score;
	}
	public int getMixed() {
		return mixed;
	}
	public void setMixed(int mixed) {
		this.mixed = mixed;
	}
	
	

}
